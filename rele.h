const byte PinRele = 11;
    
//====================================================================================================================
// INICIALIZA
//====================================================================================================================
void Rele_Inicializa()
{
    pinMode(PinRele, OUTPUT);    
    Serie_Envia('I',"Rele");
}

//====================================================================================================================
//====================================================================================================================
void Rele_ON()
{
  digitalWrite(PinRele,HIGH);
}

//====================================================================================================================
//====================================================================================================================
void Rele_OFF()
{
  digitalWrite(PinRele,LOW);
}

//====================================================================================================================
//====================================================================================================================
int  Rele_Estado()
{
  int EstadoRele = digitalRead(PinRele);
  return EstadoRele;
}

